#include "utils.h"

char	*ft_lowercase(char *str)
{
	int	i;

	i = 0;
	while ((str[i] >= 'a' && str[i] <= 'z') || (str[i] >= 'A' && str[i] <= 'Z'))
	{
		if (str[i] >= 'A' && 'Z' >= str[i])
			str[i] = str[i] + 32;
		i ++;
	}
	return (str);
}
